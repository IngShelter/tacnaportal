    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        //
        $data = Department::lists('name','id')->prepend('');
        return View('managers.create',['departments'=>$data]);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $manager = new Manager;
        $manager->firstname = $request->input('firstname');
        $manager->lastname = $request->input('lastname');
        $manager->department = $request->input('department');
        $manager->save();

        return redirect('managers');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
        $manager = Manager::findOrFail($id);
        $deptname = Department::where('id',$manager['department'])->value('name');
        return View('managers.details',['manager'=>$manager,'depto'=>$deptname]);
    }

